function fun1225() {
    let lines = getInput1225().split("\n").map(it => it.trim())

    let height = lines.length
    let width = lines[0].length

    let east = new Map<String, number[]>()
    let south = new Map<String, number[]>()


    for(let i = 0; i < height;i++)
    {
        for(let j = 0; j < width;j++)
        {
            if(lines[i][j] == "v")
            {
                let pos = [i,j]
                south.set(pos.toString(),pos)
            }
            else if(lines[i][j] == ">")
            {
                let pos = [i,j]
                east.set(pos.toString(),pos)
            }
        }
    }


    let move = true
    let i = 0
    for(; move;i++) {
        move = false
        let eastNew = new Map<String, number[]>()
        for(let [key,value] of east)
        {
            let posDestination = [value[0], (value[1] + 1) % width]
            if(!east.has(posDestination.toString()) && !south.has(posDestination.toString()))
            {
                move = true
                eastNew.set(posDestination.toString(), posDestination)
            }
            else{
                eastNew.set(key,value)
            }
        }
        east = eastNew

        let southNew = new Map<String, number[]>()
        for(let [key,value] of south)
        {
            let posDestination = [(value[0] + 1) % height, value[1]]
            if(!south.has(posDestination.toString()) && !east.has(posDestination.toString()))
            {
                move = true
                southNew.set(posDestination.toString(), posDestination)
            }
            else{
                southNew.set(key,value)
            }
        }
        south = southNew
    }
    console.log("took " + i)
}



